<template>
  <div class="search">
    <keep-alive>
      <router-view></router-view>
    </keep-alive>

    <Toast v-if="showToast" />

    <h1>Search</h1>
    <div>
      <form @submit.prevent="submitSearch">
        <input
          v-model="searchTerm"
          @submit="submitSearch"
          placeholder="Search for repo"
        />
        <input type="submit" value="Search" />
      </form>
    </div>

    <div>
      <div id="results-loading" v-if="loading">
        <div>Loading...</div>
      </div>
      <div id="results-list" v-if="!loading">
        <div v-if="results.length" id="results-header">
          <b>Repository name:</b>
          <b>Author:</b>
          <b>Amount of stars:</b>
          <b>Amount of forks:</b>
        </div>
        <div
          id="results-item"
          v-for="result in results"
          :key="result.id"
          @click.stop="linkToRepo(result.full_name)"
        >
          <div>{{ result.name }}</div>
          <div>{{ result.owner.login }}</div>
          <div>{{ result.stargazers_count }}</div>
          <div>{{ result.forks_count }}</div>
          <div
            @click.stop="
              addBookmark({ link: result.html_url, slug: result.full_name })
            "
          >
            Bookmark
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
// @ is an alias to /src
import Toast from "@/components/Toast.vue";

export default {
  name: "Search",
  components: {
    Toast
  },
  data: () => ({
    searchTerm: ""
  }),
  computed: {
    loading: function() {
      return this.$store.state.loading;
    },
    results: function() {
      return this.$store.state.results;
    },
    showToast: function() {
      return this.$store.state.showToast;
    }
  },
  watch: {},
  methods: {
    addBookmark: function(linkObject) {
      this.$store.dispatch("addBookmark", linkObject);
    },
    linkToRepo: function(slug) {
      this.$router.push({
        name: "RepositoryDetails",
        params: {
          slug
        }
      });
    },
    submitSearch: function() {
      this.$store.dispatch("searchForRepositories", this.searchTerm);
      this.searchTerm = "";
    }
  }
};
</script>

